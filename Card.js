class  Card {

    constructor(p, v)
    {
        this.point = p;
        this.variety = v;
    }

    getPoint() {
        return this.point;
    }

    setPoint(point) {
        this.point = point;
    }

    getVariety() {
        return this.variety;
    }

    setVariety(variety) {
        this.variety = variety;
    }
}
